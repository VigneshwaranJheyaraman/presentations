(ns clojure-101.scalar-type
  "i display all the scalar-types in clojure"
  {:clj-kondo/ignore [:missing-docstring
                      :clojure-lsp/unused-public-var]})



;; integer
(def int_i 1)

;; float
(def float_i (float 1))

;; double
(def double_i (double 1))

;; long
(def long_i (long 1))

;; boolean
(def bool_i true)

;; string
(def string_i "string")

;; char
(def char_i \c)

